#creando SETS para saber su funcionamiento y estructura
myFruits={"platano","manzana","mango","piña"}
friendFruit={"platano","pera","uvas","piña"}
unidos=myFruits.union(friendFruit)
inter=myFruits.intersection(friendFruit)
diferencia=myFruits.difference(friendFruit)
print(unidos)
print(inter)
print(diferencia)
